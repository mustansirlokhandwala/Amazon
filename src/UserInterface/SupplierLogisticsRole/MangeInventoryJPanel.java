    private void addQuantityJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addQuantityJButtonActionPerformed
        int selectedRow = inventoryTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an Product");
        }
        
        ProductInventory pi =(ProductInventory)inventoryTable.getValueAt(selectedRow, 0);
        
        
         
        AddQuantityJPanel addQuantityJPanel = new AddQuantityJPanel(pi, userProcessContainer);
        CardLayout layout =(CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("QuantityJPanel", addQuantityJPanel);
        layout.next(userProcessContainer);
       
    }//GEN-LAST:event_addQuantityJButtonActionPerformed

