    public void populateCart()
    {
        DefaultTableModel model = (DefaultTableModel)finalcheckoutJTable.getModel();
        model.setRowCount(0);
        
        for(OrderItem oi : customer.getCart().getOrderItemList()){
            Object[] row = new Object[6];
            row[0]=oi;
            row[1]=oi.getProductInventory().getProduct().getProductCompany();
            row[2]=oi.getProductInventory().getProduct().getProductPrice();
            row[3]=oi.getQuantity();
            row[4]=oi.Price();
            row[5]=oi.getProductInventory().getQuantity();
            model.addRow(row);
                    
        }
    }
    
    public void populateForm(){
        streetTxtfield.setText(customer.getPerson().getAddress().getStreetAddress());
        CityTxtField.setText(customer.getPerson().getAddress().getCity());
        StateTxtField.setText(customer.getPerson().getAddress().getState());
        countryTxtField.setText(customer.getPerson().getAddress().getCountry());
        zipCodetxtField.setText(String.valueOf(customer.getPerson().getAddress().getZipCode()));
        
        double  cost =0;
        for(OrderItem oi :customer.getCart().getOrderItemList()){
            cost +=oi.Price();
        }
        DecimalFormat df = new DecimalFormat("#.00");
        
        totalCosttxtField.setText(df.format(cost));
        double tax = cost * 0.065;
        
        taxTxtField.setText(String.valueOf(df.format(tax)));       
       
        double totolround = tax+cost;
        grandTotalTxtField.setText(String.valueOf(df.format(totolround)));
        
    }

