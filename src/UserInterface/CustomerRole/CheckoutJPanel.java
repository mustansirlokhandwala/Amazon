    private void checkOutJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkOutJButtonActionPerformed
        
        Invoice invoice =amazon.getCustomerInvoiceDirectory().createandAddInvoice();
        invoice.setCustomer(customer);
        Order order = new Order();
        for(OrderItem oi : customer.getCart().getOrderItemList()){
            order.addOrderItem(oi);
        }
        invoice.setOrder(order);     
        invoice.setTotal(Double.parseDouble(grandTotalTxtField.getText()));
        invoice.setTax(Double.parseDouble(taxTxtField.getText()));
        Invoice invoice1 =customer.getInvoiceDirectory().AddInvoice(invoice);
        
        
        amazon.getMasterOrderList().addOrder(order);
        amazon.getCustomerInvoiceDirectory().getInvoiceList().add(invoice);
        customer.getMasterOrderList().addOrder(order);
        
        for(OrderItem oi : customer.getCart().getOrderItemList()){
            
            OrderRequest orderRequest = new OrderRequest();
            orderRequest.setOrderItem(oi);
            orderRequest.setOrdered(true);
            orderRequest.setOrderDate(new Date());
            orderRequest.setCustomer(userAccount);
            
           Supplier sup = orderRequest.getOrderItem().getProductInventory().getProduct().getProductCompany();
            sup.getWorkQueue().addWorkRequest((WorkRequest)orderRequest);
            userAccount.getWorkQueue().addWorkRequest((WorkRequest)orderRequest);
            amazon.getWorkQueue().addWorkRequest(orderRequest);
            oi.getProductInventory().setQuantity(-oi.getQuantity());
            
         
        
        }
        
        
        
        
        
        customer.getCart().getOrderItemList().clear();
        populateCart();
        
    }//GEN-LAST:event_checkOutJButtonActionPerformed

