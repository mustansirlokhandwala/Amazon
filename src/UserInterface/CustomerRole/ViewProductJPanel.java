    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(deliveryDateTxtField.getText().equalsIgnoreCase("In Transit"))
       {
           JOptionPane.showMessageDialog(null, "The Order Has Not Been Delivered as Yet ");
           return;       
       }
       int quantity =(int)returnedQuantityJSpinner.getValue();
       OrderReturnRequest orderReturnRequest = new OrderReturnRequest();
       OrderItem oi = new OrderItem();
       oi.setProductInventory(orderReturnRequest.getOrderItem().getProductInventory());
       oi.setQuantity(quantity);
       
       orderReturnRequest.setOrderItem(oi);
       orderReturnRequest.setPurchaseDate(orderRequest.getOrderDate());
       orderReturnRequest.setReturnRequestDate(new Date());
       orderReturnRequest.setSender(userAccount);
       
       
       amazon.getWorkQueue().addWorkRequest(orderReturnRequest);
       userAccount.getWorkQueue().addWorkRequest(orderReturnRequest);
    }//GEN-LAST:event_jButton1ActionPerformed

