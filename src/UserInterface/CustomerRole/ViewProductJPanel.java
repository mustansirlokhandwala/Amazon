    private void populateform(){
        productNametxtField.setText(String.valueOf(orderItem.getProductInventory().getProduct()));
        productCompnayTxtField.setText(String.valueOf(orderItem.getProductInventory().getProduct().getProductCompany()));
        productPricetxtField.setText(String.valueOf(orderItem.getProductInventory().getProduct().getProductPrice()));
        productQuantityTxtField.setText(String.valueOf(orderItem.getQuantity()));
      
        SpinnerNumberModel model = (SpinnerNumberModel)returnedQuantityJSpinner.getModel();
        model.setMaximum(orderItem.getQuantity());
        
        for(WorkRequest wr : userAccount.getWorkQueue().getWorkQueueList()){
            if(wr instanceof OrderRequest){
                OrderRequest or = (OrderRequest)wr;
                if(or.getOrderItem().equals(orderItem)){
                    productPurchaseDateTxtField.setText(String.valueOf(or.getOrderDate()));
                    deliveryDateTxtField.setText(String.valueOf(or.getDeliveryDate() == null ?"In Transit":or.getDeliveryDate()));
                    orderRequest= or;
                    break;
                }
            }
        }
           
                
            
        
    }
    
    
   
        

