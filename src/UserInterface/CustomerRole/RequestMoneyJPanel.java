    private void RequestBankJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequestBankJButtonActionPerformed
       Bank bank = null;
        RequestMoney requestMoney = new RequestMoney();
       requestMoney.setBk(bankAccount);
       String amount = amountTxtField.getText();
       if(amount.trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Please Enter Amount");
           return;
       }
       try{
       requestMoney.setAmount(Integer.parseInt(amount));
       }catch(NumberFormatException e){
           JOptionPane.showMessageDialog(null,"Enter only Numeric Data");
           return;
       }
       for(Network countrNetwork : ecosystem.getNetworkDirectory().getNetworkList()){
           if(countrNetwork.getName().equals(customer.getPerson().getAddress().getCountry())){
               for(Enterprise enterprise : countrNetwork.getEnterpriseDirectory().getEnterpriseList()){
                   if(enterprise instanceof Bank){
                       bank = (Bank)enterprise;
                       
                   }
               }
           }
       }
       bank.getWorkQueue().addWorkRequest(requestMoney);
       
      
    }//GEN-LAST:event_RequestBankJButtonActionPerformed


