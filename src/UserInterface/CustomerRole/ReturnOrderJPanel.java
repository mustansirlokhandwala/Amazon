    private void populateOrderItemTable(){
        DefaultTableModel model = (DefaultTableModel)orderItemJtable.getModel();
        model.setRowCount(0);
        for(Order order : customer.getMasterOrderList().getOrderList()){
            for(OrderItem orderItem : order.getOrderItemList()){
                Object[] row = new Object[3];
                row[0]=orderItem;
                row[1]=orderItem.getProductInventory().getProduct().getProductCompany();
                row[2]=orderItem.getQuantity();
                model.addRow(row);
            }
        }
    }

