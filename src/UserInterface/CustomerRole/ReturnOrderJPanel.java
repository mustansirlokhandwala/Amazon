    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = orderItemJtable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an OrderItem");
        }
        OrderItem orderItem = (OrderItem )orderItemJtable.getValueAt(selectedRow, 0);
        for(WorkRequest wr : userAccount.getWorkQueue().getWorkQueueList()){
            if(wr instanceof OrderReturnRequest){
                OrderReturnRequest orr = (OrderReturnRequest)wr;
                if(orr.getOrderItem().equals(orderItem)){
                    JOptionPane.showMessageDialog(null, "The Order has Already been request for return . Please check its status");
                    return;
                }
            }
        }
        
        ViewProductJPanel viewProductJPanel = new ViewProductJPanel(userAccount, userProcessContainer, customer, amazon, orderItem);
        CardLayout layout =(CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("ViewProduct", viewProductJPanel);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed


