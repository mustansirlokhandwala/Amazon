    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedRow = paymentTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a order");
            return;
        }
        Invoice invoice = (Invoice)paymentTable.getValueAt(selectedRow, 0);
        if(invoice.isPaid()){
            JOptionPane.showMessageDialog(null, "Payment has Already Been made");
        }
        AmazonProcessPayementJPanel amazonProcessPayementJPanel = new AmazonProcessPayementJPanel(invoice, userProcessContainer, bank, amazon, ecoSystem, bankAccount);
        CardLayout layout = (CardLayout )userProcessContainer.getLayout();
        userProcessContainer.add("Amazon Process Payment ",amazonProcessPayementJPanel);
        layout.next(userProcessContainer);
        
    }//GEN-LAST:event_jButton1ActionPerformed

