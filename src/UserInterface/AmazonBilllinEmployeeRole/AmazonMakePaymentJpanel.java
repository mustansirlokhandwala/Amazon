    public AmazonMakePaymentJpanel(JPanel upc , EcoSystem ecoSystem ,Amazon amazon ) {
        initComponents();
        this.userProcessContainer = upc ;
        this.ecoSystem = ecoSystem;        
        this.amazon = amazon;
        this.bankAccount = bankAccount;
        for(Network CountryNetwork : ecoSystem.getNetworkDirectory().getNetworkList()){
            if(CountryNetwork.getName().equals(amazon.getAddress().getCountry())){
                for(Enterprise enterprise : CountryNetwork.getEnterpriseDirectory().getEnterpriseList()){
                    if(enterprise.getEnterpriseType().equals(EnterpriseType.Bank)){
                        this.bank=(Bank)enterprise;
                        
                    }
                }
            }
        }
        for(BankAccount bk : this.bank.getBankAccountDirectory().getBankAccountList()){
            if(bk.getEnterprise() != null){
                if(bk.getEnterprise().equals(this.amazon)){
                    this.bankAccount=bk;
                }
                    
            }
        }
        populatePayment();
    }
    
    
    private void populatePayment(){
        DefaultTableModel model =(DefaultTableModel)paymentTable.getModel();
        model.setRowCount(0);
        
        for(Invoice invoice : amazon.getSupplierInvoiceDirectory().getInvoiceList()){
            Object[] row = new Object[4];
            row[0]=invoice;
            row[1]=invoice.getTotal();
            row[2]=invoice.getPayment().getAmountPaid();            
            row[3]=invoice.isPaid() == true ? "Amount Paid" : "Pending";
            model.addRow(row);
                    
        }
    }

