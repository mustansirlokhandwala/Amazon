     private void populatedeliverTable(){
        DefaultTableModel model = (DefaultTableModel)deliveryJTable.getModel();
        model.setRowCount(0);
        
        for(WorkRequest wr : deliveryVans.getDeliveryQueue().getWorkQueueList()){
            
            if(wr instanceof OrderRequest){
                OrderRequest or = (OrderRequest)wr;
                Object[] row = new Object[5];
            row[0]=or;
            row[1]=or.getCustomer().getPerson().getAddress();
            row[2]=or.getShipmentDate();
            row[3]=or.isDelivered() == false ? "Not Delivered  " : or.getDeliveryDate();
            row[4]=or.getDeliveryEmployee() == null ? " Not assigned" : or.getDeliveryEmployee();
            
            model.addRow(row);
            }
            else if(wr instanceof OrderReturn){
                OrderReturn or = (OrderReturn)wr;
                Object[] row = new Object[5];
            row[0]=or;
            row[1]=or.getCustomer().getPerson().getAddress();
            row[2]=or.getCustomerDropOff();
            row[3]=or.getDeliveryDate() == null ? "Not Delivered  " : or.getDeliveryDate();
            row[4]=or.getDeliveryEmployee() == null ? " Not assigned" : or.getDeliveryEmployee();
            
            model.addRow(row);
            }
            
    
                    
            
        }  
            
        }

