    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedItem = deliveryJTable.getSelectedRow();
        if(selectedItem < 0){
            JOptionPane.showMessageDialog(null, "Please select a order to be Delivered");
            return;
        }
        WorkRequest wr = (WorkRequest)deliveryJTable.getValueAt(selectedItem, 0);
        if(wr instanceof OrderRequest){
        OrderRequest or = (OrderRequest)deliveryJTable.getValueAt(selectedItem, 0);
        or.setDelivered(true);
        or.setDeliveryDate(new Date());    
        }
        else if (wr instanceof OrderReturn){
        OrderReturn or = (OrderReturn)deliveryJTable.getValueAt(selectedItem, 0);
        or.setDeliveryDate(new Date());
        
            
        }
        
        populatedeliverTable();

    }//GEN-LAST:event_jButton1ActionPerformed

