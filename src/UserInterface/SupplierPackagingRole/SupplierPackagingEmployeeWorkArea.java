    private void assignToMeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignToMeButtonActionPerformed
        int selectedRow = packagingJobTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a order to be assigned ");
        }
        
        OrderRequest or= (OrderRequest)packagingJobTable.getValueAt(selectedRow, 0);
        if(or.getPackagingEmployee() != null){
            JOptionPane.showMessageDialog(null, "The orderRequest has already been assigned to an employeee");
            return;
        }
        
        or.setPackagingEmployee(userAccount);
        populatePackagingJobTable();
    }//GEN-LAST:event_assignToMeButtonActionPerformed

    private void prcoessorderButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prcoessorderButtonActionPerformed
        int selectedItem = packagingJobTable.getSelectedRow();
        if(selectedItem < 0){
            JOptionPane.showMessageDialog(null,"Please select a Job to be process");
            return;
        }
        OrderRequest or = (OrderRequest)packagingJobTable.getValueAt(selectedItem, 0);
        if(!or.getPackagingEmployee().equals(userAccount)){
            JOptionPane.showMessageDialog(null, "The order is not assigned to you ");
            return;
        }
        if(or.isPackaged()){
            JOptionPane.showMessageDialog(null, "The order is already Processed");
            return;
        }
        ProcessOrderItemJPanel processOrderItemJPanel = new ProcessOrderItemJPanel(userProcessContainer, or, ecoSystem, supplier, userAccount);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("Process order", processOrderItemJPanel);
        layout.next(userProcessContainer);
        
                
    }//GEN-LAST:event_prcoessorderButtonActionPerformed

