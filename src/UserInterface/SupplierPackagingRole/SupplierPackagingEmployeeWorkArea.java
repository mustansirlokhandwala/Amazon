    public void populatePackagingJobTable(){
        DefaultTableModel model = (DefaultTableModel)packagingJobTable.getModel();
        model.setRowCount(0);
        for(WorkRequest wr : supplier.getWorkQueue().getWorkQueueList()){
            if(wr instanceof OrderRequest){
                OrderRequest or = (OrderRequest)wr;
            Object[] row = new Object[6];
            row[0]=or;
            row[1]=or.getOrderItem().getProductInventory().getProduct().getProductCompany();
            row[2]=or.getOrderItem().getQuantity();
            row[3]=or.getOrderDate();
            row[4]=or.isPackaged() == false ? "Not Processes" : or.getPackageDate();
            row[5]=or.getPackagingEmployee() == null ? "Not assigned" : or.getPackagingEmployee();
            model.addRow(row);
            }
            
        }
    }

