    public void populateBankTable()
    {
        ArrayList<Enterprise> BankList = new ArrayList<Enterprise>();
        for(Network network : ecoSystem.getNetworkDirectory().getNetworkList()){
            for(Enterprise enterprise :network.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprise.getEnterpriseType().equals(EnterpriseType.Bank)){
                    BankList.add(enterprise);
                }
            }               
                
        }
        DefaultTableModel model = (DefaultTableModel)bankTable.getModel();
        model.setRowCount(0);
        for(Enterprise enterprise : BankList){
            Object[] row = new Object[2];
            row[0]= enterprise;
            row[1]= enterprise.getAddress().getCountry();
            model.addRow(row);
            
                    
        }
    }

