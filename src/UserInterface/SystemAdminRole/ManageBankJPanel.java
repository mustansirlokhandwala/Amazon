    private void createHeadQuaterAdminJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createHeadQuaterAdminJButtonActionPerformed
        int selectedItem = bankTable.getSelectedRow();
        if(selectedItem < 0){
            JOptionPane.showMessageDialog(null,"Please Select an HeadQuaters");
            return;
        }
        
        Bank bank = (Bank)bankTable.getValueAt(selectedItem, 0);
        for(UserAccount ua : bank.getUserAccountDirectory().getUserAccountList()){
            if(ua.getRole() instanceof BankAdminRole){
                JOptionPane.showMessageDialog(null, "An Admin for this Bank Enterprise Already Exists");
                return;
            }
        }
        CreateBankAdminJPanel1 adminJPanel1 = new CreateBankAdminJPanel1(bank, userProcessContainer, ecoSystem);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("createBankAdmin",adminJPanel1);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_createHeadQuaterAdminJButtonActionPerformed

