    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectedrow = returnRequestTable.getSelectedRow();
        if(selectedrow < 0){
            JOptionPane.showMessageDialog(null, "Please select an request");
            return;
        }
        OrderReturnRequest orr = (OrderReturnRequest)returnRequestTable.getValueAt(selectedrow, 0);
        if(orr.getStatus() != null){
            JOptionPane.showMessageDialog(null, "The request has been processed");
            return;
        }
        returnSacntionJPanel sacntionJPanel = new returnSacntionJPanel(userProcessContainer, userAccount, amazon, orr);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("Sanction request",sacntionJPanel);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

