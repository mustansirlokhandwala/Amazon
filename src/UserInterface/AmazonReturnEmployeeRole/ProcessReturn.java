     private void populatetable(){
        DefaultTableModel model = (DefaultTableModel)returnRequestTable.getModel();
        model.setRowCount(0);
        for(WorkRequest wr : userAccount.getWorkQueue().getWorkQueueList()){
            if(wr instanceof OrderReturnRequest){
                OrderReturnRequest orr = (OrderReturnRequest)wr;
                Object[] row = new Object[6];
                row[0]=orr;
                row[1]=orr.getOrderItem().getProductInventory().getProduct().getProductCompany();
                row[2]=orr.getOrderItem().getQuantity();
                row[3]=orr.getPurchaseDate();
                row[4]=orr.getReturnRequestDate();
                row[5]=orr.getStatus() == null ? "Pending" : orr.getStatus();
                model.addRow(row);
            }
        }
        
    
    }

