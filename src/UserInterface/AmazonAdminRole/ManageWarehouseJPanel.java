    public void populateLocationComboBox(){
        locationComboBox.removeAllItems();
        locationComboBox.addItem("All States");
        Network inCountry = null;
        for(Network countryNetwork : ecoSystem.getNetworkDirectory().getNetworkList()){
            if(countryNetwork.getName().equals(amazon.getAddress().getCountry())){
            inCountry = countryNetwork;
            }            
        }
        for(Network stateNetwork :inCountry.getNetworkDirectory().getNetworkList()){
            locationComboBox.addItem(stateNetwork);
        }
    }
    public void populateWarehouseTable(String state){
        DefaultTableModel model = (DefaultTableModel)warehouseTable.getModel();
        model.setRowCount(0);
        for(WareHouse wareHouse : amazon.getWarehouseDirectory().getWareHouseList()){
            if(wareHouse.getAddress().getState().equals(state) || state.equalsIgnoreCase("All States")){
            Object[] row = new Object[3];
            row[0]=wareHouse;
            row[1]=wareHouse.getEnterpriseID();
            row[2]=wareHouse.getAddress();
            model.addRow(row);
            }
            
            
        }
    }

