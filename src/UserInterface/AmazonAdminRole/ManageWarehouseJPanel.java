    private void createWareHouseAdminJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createWareHouseAdminJButtonActionPerformed
        int selectRow = warehouseTable.getSelectedRow();
        if(selectRow < 0){
            JOptionPane.showMessageDialog(null, "Please Select a Warehouse");            
        }
        WareHouse wareHouse = (WareHouse)warehouseTable.getValueAt(selectRow, 0);
        for(UserAccount ua : wareHouse.getUserAccountDirectory().getUserAccountList()){
            if(ua.getRole() instanceof  WarehouseAdmin){
                JOptionPane.showMessageDialog(null, "An Admin for this WareHouse already exists");
                return;
            }
        }
        WarehouseAdminJPanel warehouseAdminJPanel = new WarehouseAdminJPanel(wareHouse, userProcessContainer, ecoSystem);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("WarehouseAdmin", warehouseAdminJPanel);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_createWareHouseAdminJButtonActionPerformed

