    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double amount = Double.parseDouble(amountGrantedTxtField.getText());
        BankAccount debit = null;
        for(BankAccount bankAccount : bank.getBankAccountDirectory().getBankAccountList()){
            if(bankAccount.getEnterprise() != null){
                if(bankAccount.getEnterprise().equals(bank)){
                    debit= bankAccount;
                    
                }
            }
        }
        BankAccount credit = requestMoney.getBk();
        Transaction transaction = new Transaction();
        transaction.setAmount(amount);
        transaction.setCreditAccount(credit);
        transaction.setDebitAccount(debit);
        transaction.setType("Credit Increase");
        credit.getTransactionList().add(transaction);
        debit.getTransactionList().add(transaction);
        bank.getBankAccountDirectory().peformBanktransaction(transaction);
        requestMoney.setStatus("Completed");
    }//GEN-LAST:event_jButton1ActionPerformed

