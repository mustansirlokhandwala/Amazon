    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int selectRow = invoicerequest.getSelectedRow();
        if(selectRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an invoice ");
            return;
        }
        InvoiceGenerationRequest ivr = (InvoiceGenerationRequest)invoicerequest.getValueAt(selectRow, 0);
        if(ivr.isGenerated()){
            JOptionPane.showMessageDialog(null, "The invoice has been generated ");
           return;
        }
        GenerateInvoiceJPanel generateInvoiceJPanel = new GenerateInvoiceJPanel(userProcessContainer, supplier, ivr, ecoSystem, userAccount);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.add("Invoice generation",generateInvoiceJPanel);
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed

