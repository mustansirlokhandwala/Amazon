    private void populateInvoicetable(){
        DefaultTableModel model = (DefaultTableModel)invoicerequest.getModel();
        model.setRowCount(0);
        for(WorkRequest wr : supplier.getWorkQueue().getWorkQueueList()){
            if(wr instanceof InvoiceGenerationRequest){
                InvoiceGenerationRequest ivr =(InvoiceGenerationRequest)wr;
            Object[] row = new Object[5];
            row[0]=ivr;
            row[1]=ivr.getOrderItem().getProductInventory().getProduct().getProductPrice();
            row[2]=ivr.getOrderItem().getQuantity();
            row[3]=ivr.isGenerated() == false ? "Pending" : "Invoice Sent";
            row[4]=ivr.getReceiver() == null ? "Not Assigned" : ivr.getReceiver();
            model.addRow(row);
            }
            
        }
    }

