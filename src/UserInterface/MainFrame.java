    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        //To remove all existing panels 
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        userProcessContainer.removeAll();
        
        //To validate username and password
        String userName = usernameTxtField.getText();
        char[] passwordArray = passwordTxtField.getPassword();
        String password = String.valueOf(passwordArray);
        
        
        
        UserAccount ua = ecoSystem.getUserAccountDirectory().authenticateUser(userName, password);
       if(ua != null){
        userProcessContainer.add("workArea",ua.getRole().createWorkArea(ecoSystem, userProcessContainer, null, null, ua));
        layout.next(userProcessContainer);
        return;
           
       }
        Enterprise inEnterprise = null;
        Organization inOrganization = null;
        for(Network countrynetwork : ecoSystem.getNetworkDirectory().getNetworkList()){
            for(Enterprise enterprise : countrynetwork.getEnterpriseDirectory().getEnterpriseList()){
                ua=enterprise.getUserAccountDirectory().authenticateUser(userName, password);                
                if(ua != null){
                    inEnterprise = enterprise;
                    break;
                }
                else{
                    for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationlist()){
                        ua = organization.getUserAccountDirectory().authenticateUser(userName, password);
                        if(ua != null){
                            inEnterprise = enterprise;
                            inOrganization = organization;
                            break;
                        }
                    }
                }
                if(inOrganization != null){
                    break;
                }
            }
             if(inEnterprise != null){
                 break;
             }   
                    
                               
                
            
            for(Network stateNetwork : countrynetwork.getNetworkDirectory().getNetworkList()){
                for(Enterprise enterprise: stateNetwork.getEnterpriseDirectory().getEnterpriseList()){
                    ua=enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    if(ua != null){
                        inEnterprise = enterprise;
                        break;
                    }
                    else {
                        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationlist()){
                            ua = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if(ua != null){
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                    }
                    if(inOrganization != null){
                        break;
                    }
                }
                 if(inEnterprise !=null){
                break;
            }
                for(Network cityNetwork : stateNetwork.getNetworkDirectory().getNetworkList()){
                    for(Enterprise enterprise : cityNetwork.getEnterpriseDirectory().getEnterpriseList()){
                        ua = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                        if(ua != null){
                            inEnterprise = enterprise;
                            break;
                        }
                        else{
                            for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationlist()){
                                ua = organization.getUserAccountDirectory().authenticateUser(userName, password);
                                if(ua != null){
                                    inEnterprise = enterprise;
                                    inOrganization = organization;
                                    break;
                                            
                                }
                            }
                            if(inOrganization != null){
                                break;
                            }
                        }
                    }if(inEnterprise != null){
                    break;
                }
                }
               
            }
            
        }
        
       
        if(ua == null){
            JOptionPane.showMessageDialog(null,"Invalid Credentials");
            return;
            
            
        } 
        
        
        userProcessContainer.add("workArea",ua.getRole().createWorkArea(ecoSystem, userProcessContainer, inOrganization, inEnterprise, ua));
        layout.next(userProcessContainer);
        usernameTxtField.setText("");
        passwordTxtField.setText("");
        
            
    }//GEN-LAST:event_LoginButtonActionPerformed

