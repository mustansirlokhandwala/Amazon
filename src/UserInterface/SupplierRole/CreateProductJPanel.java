    private void createProductJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createProductJButtonActionPerformed
       try{
        String name = productNameTxtField.getText();
       double price = Double.parseDouble(productPriceTxtField.getText());
       String type = new String();
       try{
       type = (String)ProductTypeComboBox.getSelectedItem();
       if(type.trim().isEmpty())
       {
           
       }
       }catch(NullPointerException np){JOptionPane.showMessageDialog(null, "Please seltect a type");return;}
       double volume = Double.parseDouble(ProductVolumetxtField.getText());
       if(name.trim().isEmpty()){
           JOptionPane.showMessageDialog(null, "Please complete all fields");
       }
       Product p = supplier.getProductCatalog().createandAddProduct();
       p.setName(name);
       p.setProductPrice(price);
       p.setProductCompany(supplier);
       p.setProductType(type);
       p.setProductVolume(volume);
       supplier.getInventoryList().addProductInventory(p);
       }catch(NumberFormatException e ){JOptionPane.showMessageDialog(null,"enter numeric values in volume and price");return;}
       
    }//GEN-LAST:event_createProductJButtonActionPerformed

