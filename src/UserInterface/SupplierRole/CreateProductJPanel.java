    public CreateProductJPanel(JPanel upc , Supplier supplier , EcoSystem ecoSystem) {
        initComponents();
        this.userProcessContainer = upc ;
        this.supplier = supplier;
        this.ecosystem = ecoSystem;
        
        for(Network countrynetwork : ecoSystem.getNetworkDirectory().getNetworkList()){
            for(Enterprise enterprise : countrynetwork.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprise instanceof Amazon){
                    if(enterprise.getAddress().getCountry().equals(supplier.getAddress().getCountry())){
                        this.amazon = (Amazon)enterprise;
                        break;
                    }
                }
            }if(this.amazon != null){break;}
        }
        populateType();
    }
    
    public void populateType(){
        ProductTypeComboBox.removeAllItems();
        for(String type : amazon.getProductTypeDirectory().getProducttypeList()){
            ProductTypeComboBox.addItem(type);
        }
    }

